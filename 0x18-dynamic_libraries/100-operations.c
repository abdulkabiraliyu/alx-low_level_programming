#include <stdio.h>

/**
 * add - add two integers
 *
 * @a: parameter one
 * @b: parameter two
 *
 * Return: total
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * mul - multiply two integers
 *
 * @a: parameter one
 * @b: parameter two
 *
 * Return: total
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * sub - subtract two integers
 *
 * @a: parameter one
 * @b: parameter two
 *
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * div - divide two integers
 *
 * @a: parameter one
 * @b: parameter two
 *
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - modulo division of two integers
 *
 * @a: parameter one
 * @b: parameter two
 *
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
